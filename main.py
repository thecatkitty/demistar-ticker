import time
from neopixel import Neopixel
 
PIXELS = 32

led_strip = Neopixel(PIXELS, 0, 11, "GRB")
wheel = [
    (0xFF, 0x00, 0x00), (0xFF, 0x55, 0x00), (0xFF, 0xAA, 0x00),
    (0xFF, 0xFF, 0x00), (0xAA, 0xFF, 0x00), (0x55, 0xFF, 0x00),
    (0x00, 0xFF, 0x00), (0x00, 0xFF, 0x55), (0x00, 0xAA, 0xAA),
    (0x00, 0x55, 0xFF), (0x00, 0x00, 0xFF), (0x00, 0x00, 0xFF),
    (0x00, 0x00, 0xFF), (0x55, 0x00, 0xFF), (0xAA, 0x00, 0xFF),
    (0xFF, 0x00, 0xFF)
]

index = 0

for pixel in wheel:
    led_strip.set_pixel(index, (pixel[0] >> 1, pixel[1] >> 1, pixel[2] >> 1))
    index = index + 1

for pixel in wheel:
    led_strip.set_pixel(index, (pixel[0] >> 2, pixel[1] >> 2, pixel[2] >> 2))
    index = index + 1
 
while True:
    led_strip.show()
    time.sleep(1)
